package com.aplicatie.spintm;

public class Informatii {

    private int Image;
    private String Informatii;

    public Informatii(int Image, String Informatii) {
        this.Image = Image;
        this.Informatii = Informatii;
    }

    public int getImage() {
        return Image;
    }

    public String getInformatii() {
        return Informatii;
    }
}
